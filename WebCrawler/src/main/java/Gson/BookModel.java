package Gson;

public class BookModel {
    private String id;
    private String language;
    private String edition;
    private String author;

    public String getId() {
        return id;
    }

    public String getLanguage() {
        return language;
    }

    public String getEdition() {
        return edition;
    }

    public String getAuthor() {
        return author;
    }
}
